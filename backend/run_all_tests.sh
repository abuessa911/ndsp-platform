#!/bin/bash

echo "======================================================"
echo "🚀 بدء اختبار منظومة NDSP V4.1 بالكامل (آلياً)"
echo "======================================================"

# 1. تشغيل المستهلكين (Consumers) في الخلفية
echo "🎧 1. جاري تشغيل مستهلك التوقيت ومستهلك السيناريو في الخلفية..."
./node_modules/.bin/tsx src/test_consumer.ts &
CONSUMER_PID=$!

./node_modules/.bin/tsx src/test_scenario.ts &
SCENARIO_PID=$!

# ننتظر ثانيتين لنتأكد أن المستهلكين في وضع الاستعداد
sleep 2
echo "------------------------------------------------------"

# 2. ضخ حدث التوقيت (Layer 3)
echo "📡 2. جاري ضخ حدث التوقيت (Timing Authority)..."
./node_modules/.bin/tsx src/test_timing.ts
sleep 2 # ننتظر ليقوم المستهلك بالتقاطه وطباعته
echo "------------------------------------------------------"

# 3. ضخ حدث القرار النهائي (Layer 15)
echo "📡 3. جاري تجميع وضخ القرار النهائي (Final Decision)..."
./node_modules/.bin/tsx src/test_final_decision.ts
sleep 3 # ننتظر ليقوم محرك السيناريو بالتقاطه وطباعته
echo "------------------------------------------------------"

# 4. التنظيف والإغلاق
echo "🧹 4. اكتمل الاختبار! جاري إغلاق المستهلكين في الخلفية..."
kill $CONSUMER_PID
kill $SCENARIO_PID

echo "======================================================"
echo "✅ نجاح: تم اختبار جميع الطبقات (التوقيت، التجميع، السيناريو) في أمر واحد!"
echo "======================================================"
